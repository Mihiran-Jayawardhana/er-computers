    public void loadCustomerDetails() {

        String invoice_idForUser = jLabel29.getText();

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `invoice` INNER JOIN `customer` ON `invoice`.`customer_id`=`customer`.`id` WHERE `invoice`.`unique_id`='" + invoice_idForUser + "'");

            if (rs.next()) {

                customer_id = rs.getString("customer.id");
                customer_name = rs.getString("customer.name");
                customer_mobile = rs.getString("customer.mobile");

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public static void leftItemCount(String invoiceId) {

        jPanel6.setVisible(false);

        String purchQTY = jLabel11.getText();
        String proid = jLabel5.getText();
        String proname = jLabel8.getText();

        try {

            DefaultTableModel def = (DefaultTableModel) jTable2.getModel();
            def.setRowCount(0);

            ResultSet rs = MySQL.search("SELECT * FROM `return` WHERE `return`.`invoiceID`='" + invoiceId + "' AND `return`.`prod_id`='" + proid + "' AND `return`.`prod_name`='" + proname + "'  ");

            if (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("invoiceID"));
                v.add(rs.getString("prod_id"));
                v.add(rs.getString("prod_name"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("amount") + "");

                returnedQty = Integer.parseInt(rs.getString("qty"));
                def.addRow(v);

                leftQuantity = Integer.parseInt(purchQTY) - returnedQty;

                if (returnedQty == 0) {
                    jLabel14.setText(purchQTY);

                } else {

                    jLabel14.setText(leftQuantity + "");

                }

                jTable2.setModel(def);

            } else {
                jLabel14.setText(purchQTY);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

