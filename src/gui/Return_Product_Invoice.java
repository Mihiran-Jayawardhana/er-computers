    public void printReturnInvoice() {

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd  HH:mm:ss");
        Date dT = new Date();
        String dat = sdf.format(dT);

        try {

            InputStream filepath = getClass().getResourceAsStream("/reportreturn/return_invoice.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(filepath);

            HashMap parameters = new HashMap();

            parameters.put("Parameter1", jLabel29.getText());
            parameters.put("Parameter2", dat);
            parameters.put("Parameter3", jLabel25.getText());
            parameters.put("Parameter4", customer_id);
            parameters.put("Parameter5", customer_name);
            parameters.put("Parameter6", customer_mobile);
            parameters.put("Parameter7", jLabel22.getText());
            parameters.put("Parameter8", jLabel22.getText());
            parameters.put("Parameter8", jLabel25.getText());

            TableModel tb = jTable1.getModel();

            JRTableModelDataSource datasource = new JRTableModelDataSource(tb);

            JasperPrint jp = JasperFillManager.fillReport(jr, parameters, datasource);
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

