    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (jLabel5.getText().equals("None")) {
            JOptionPane.showMessageDialog(this, "Go back to return product page?", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            String prod_idR = jLabel5.getText();
            String prod_nameR = jLabel8.getText();
            prod_qtyR = jTextField1.getText();
            String item_priceR = jLabel32.getText();
            String availableQTY = jLabel19.getText();
            String purchedQty = jLabel14.getText();

            if (prod_qtyR.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter how many item you want to return?", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (!Pattern.compile("^[1-9][0-9]*$").matcher(prod_qtyR).matches()) {
                JOptionPane.showMessageDialog(this, "Invalid Quantity", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                if (Integer.parseInt(purchedQty) < Integer.parseInt(prod_qtyR)) {

                    JOptionPane.showMessageDialog(this, "you cannot get extra quantity in this page", "Warning", JOptionPane.WARNING_MESSAGE);

                } else if (Integer.parseInt(availableQTY) <= Integer.parseInt(prod_qtyR)) {

                    JOptionPane.showMessageDialog(this, "not enough stock quantity", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    Vector v = new Vector();

                    v.add(prod_idR);
                    v.add(prod_nameR);
                    v.add(prod_qtyR);
                    v.add(item_priceR);
                    v.add(Integer.parseInt(prod_qtyR) * Double.parseDouble(item_priceR) + "");

                    dtm.addRow(v);

                    jTable1.setModel(dtm);

                    jLabel22.setText(prod_qtyR);
                    jLabel25.setText(Integer.parseInt(prod_qtyR) * Double.parseDouble(item_priceR) + "");

                    jButton1.setEnabled(false);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

