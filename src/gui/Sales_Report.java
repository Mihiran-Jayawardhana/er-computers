    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int rowCount = jTable1.getRowCount();

        if (rowCount == 0) {
            JOptionPane.showMessageDialog(this, "nothing to print");
        } else {
            int option = JOptionPane.showConfirmDialog(this, "Do you want to print?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

            if (option == JOptionPane.YES_OPTION) {

                try {

                    String reportUrl = "/reports/salesReport.jasper";
                    InputStream reportFile = null;
                    reportFile = getClass().getResourceAsStream(reportUrl);

                    Map data = new HashMap();

                    data.put("Parameter1", Admin_Panel.fromDateN);
                    data.put("Parameter2", Admin_Panel.toDateN);
                    data.put("Parameter3", jLabel3.getText());
                    data.put("Parameter4", jLabel5.getText());

                    JRTableModelDataSource datasource = new JRTableModelDataSource(jTable1.getModel());

                    JasperPrint print = JasperFillManager.fillReport(reportFile, data, datasource);
                    JasperViewer Jviewer = new JasperViewer(print, false);
                    Jviewer.setVisible(true);

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

