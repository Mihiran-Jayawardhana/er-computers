    public static void loadSalesReportTable(String fromDateN, String toDateN) {

        DecimalFormat df = new DecimalFormat();

        try {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            ResultSet rs = MySQL.search("SELECT * FROM invoice_item INNER JOIN stock ON invoice_item.stock_id=stock.id INNER JOIN invoice ON invoice_item.invoice_id=invoice.id INNER JOIN product ON stock.product_id=product.id INNER JOIN category ON product.category_id=category.id INNER JOIN brand ON product.brand_id=brand.id WHERE DATE(date_time) BETWEEN '" + fromDateN + "' AND '" + toDateN + "' ORDER BY product.name ASC");

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("product.id"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("category.name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("invoice_item.qty"));
                v.add(rs.getString("invoice_item.item_price"));
                v.add(Integer.parseInt(rs.getString("invoice_item.qty")) * Double.parseDouble(rs.getString("invoice_item.item_price")) + "");
                dtm.addRow(v);

                double qtotal = 0;
                for (int k = 0; k < dtm.getRowCount(); k++) {
                    String t = jTable1.getValueAt(k, 4).toString();
                    qtotal = qtotal + Double.parseDouble(t);
                }
                jLabel3.setText(df.format(qtotal));

                double total = 0;
                for (int l = 0; l < dtm.getRowCount(); l++) {
                    String t = jTable1.getValueAt(l, 6).toString();
                    total = total + Double.parseDouble(t);
                }
                jLabel5.setText(df.format(total));

            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

