    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String categoryName = jComboBox1.getSelectedItem().toString();
        String brandName = jComboBox2.getSelectedItem().toString();

        String proName = jTextField1.getText();

        if (categoryName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select category", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (brandName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select brand", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (proName.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter product name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = MySQL.search("SELECT * FROM `category` WHERE `category`.`name`='" + categoryName + "'");
                rs1.next();
                String category_ID = rs1.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `brand` WHERE `brand`.`name`='" + brandName + "'");
                rs2.next();
                String brand_ID = rs2.getString("id");

                ResultSet rs3 = MySQL.search("SELECT * FROM `product` WHERE `product`.`name`='" + proName + "' AND `product`.`category_id`='" + category_ID + "' AND `product`.`brand_id`='" + brand_ID + "' ");

                if (rs3.next()) {
                    JOptionPane.showMessageDialog(this, "Please product is already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `product`(`name`,`category_id`,`brand_id`) VALUES('" + proName + "','" + category_ID + "','" + brand_ID + "')");

                    JOptionPane.showMessageDialog(this, "New Product Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                    if (cgrn != null) {
                        cgrn.loadProducts();
                        cgrn.loadBrands();
                        cgrn.loadCategories();
                    }
                     if (adminPanel != null) {
                        adminPanel.loadAllProducts();

                    }
                    this.dispose();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

